    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            switch (position) {
                case 0:
                    Tab1Data tab1 = new Tab1Data();
                    return tab1;
                case 1:
                    Tab2Today tab2 = new Tab2Today();
                    return tab2;
                case 2:
                    Tab3Tomorrow tab3 = new Tab3Tomorrow();
                    return tab3;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "DATA";
                case 1:
                    return "TODAY";
                case 2:
                    return "TOMORROW";
            }
            return null;
        }
    }
